void AppPwm_Init(void)
{
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_3);
}

void AppPwm_Set(uint8_t channel, uint8_t value)
{
	switch (channel) {
		case APP_PWM_COLOR_R:
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 100 - value);


			break;
		case APP_PWM_COLOR_G:
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 100 - value);
			break;
		case APP_PWM_COLOR_B:
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 100 - value);
			break;
		default:
			break;
	}
}

